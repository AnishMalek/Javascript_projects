let Grade=prompt("Enter your Grade");

if(Grade=='E' || Grade=='e'){
    alert("You have Choosen: Excellent ")
}
else if(Grade=='V' || Grade=='v'){
    alert("You have Choosen: Very Good");
    
}

else if(Grade=='G' || Grade=='g'){
    alert("You have Choosen: Good");
    
}
else if(Grade=='A' || Grade=='a'){
    alert("You have Choosen: Average");
    
}
else if(Grade=='F' || Grade=='f'){
    alert("You have Choosen: Fail");
    
}


else{
    alert("invalid input");
    
}