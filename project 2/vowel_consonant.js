let choice=prompt("Enter word to check Entered Word is vowel or Consonant")

if( choice=="A" || choice=="a" ||
   choice =="E" || choice=="e" ||
   choice =="I" || choice=="i" ||
   choice =="O" || choice=="o" ||
   choice =="U" || choice=="u") {

    console.log(`${choice} is a vowel`);
    
} else{
    console.log(`${choice} is a Consonant`);
    
}